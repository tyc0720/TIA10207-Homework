package hw1;

public class hw106 {
	// 請寫一隻程式,利用System.out.println()印出以下三個運算式結果:
	public static void main(String[] args) {

		// 5+5
		System.out.println(5 + 5); // 常數相加
		// 5+'5'
		System.out.println(5 + '5'); // 5為常數；'5'為字元，5的Unicode編號為53，故5+53為58
		// 5+"5"
		System.out.println(5 + "5"); // 5為常數，"5"為字串
	}

}
